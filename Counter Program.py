##THE COUNTER PROGRAM
#This program counts the numbers as desired by the programmer

print("Counting ... ")

print("\n")

#Counting Forwards
for i in range(21):
    print(i, end="")

print("\n")

#Counting Backwards
for i in range(21, 0, -1):
    print(i, end="")

    print("\n")

#Counting By Fives
for i in range(0, 21, 5):
    print(i, end="")
